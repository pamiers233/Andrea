// ====== PHASE 3d: Network topology canvas ======
function startNetworkMap() {
    const canvas = document.getElementById('net-canvas');
    const ctx = canvas.getContext('2d');
    const rect = canvas.getBoundingClientRect();
    canvas.width = rect.width * 2;
    canvas.height = rect.height * 2;
    ctx.scale(2, 2);
    const w = rect.width, h = rect.height;

    // Generate random nodes
    const nodes = [];
    for (let i = 0; i < 18; i++) {
        nodes.push({
            x: 15 + Math.random() * (w - 30),
            y: 15 + Math.random() * (h - 30),
            r: 2 + Math.random() * 3,
            pulse: Math.random() * Math.PI * 2,
            speed: 0.02 + Math.random() * 0.03,
        });
    }
    // Generate links (nearest neighbors)
    const links = [];
    nodes.forEach((a, i) => {
        const dists = nodes.map((b, j) => ({ j, d: Math.hypot(a.x - b.x, a.y - b.y) }))
            .filter(x => x.j !== i).sort((a, b) => a.d - b.d);
        for (let k = 0; k < Math.min(2, dists.length); k++) {
            const key = [Math.min(i, dists[k].j), Math.max(i, dists[k].j)].join('-');
            if (!links.find(l => l.key === key)) links.push({ a: i, b: dists[k].j, key });
        }
    });

    // Update stats
    const statsEl = document.getElementById('net-stats');
    statsEl.innerHTML = `
        <div>NODES: <span class="cyan">${nodes.length}</span></div>
        <div>LINKS: <span class="cyan">${links.length}</span></div>
        <div>LATENCY: <span class="cyan">${(5 + Math.random() * 20).toFixed(1)}ms</span></div>
        <div>PACKETS: <span class="cyan">${Math.floor(Math.random() * 50000)}</span></div>
    `;

    function drawNet() {
        ctx.clearRect(0, 0, w, h);
        // Links
        ctx.strokeStyle = 'rgba(0,229,255,0.15)';
        ctx.lineWidth = 0.5;
        links.forEach(l => {
            ctx.beginPath();
            ctx.moveTo(nodes[l.a].x, nodes[l.a].y);
            ctx.lineTo(nodes[l.b].x, nodes[l.b].y);
            ctx.stroke();
        });
        // Nodes
        nodes.forEach(n => {
            n.pulse += n.speed;
            const glow = 0.4 + Math.sin(n.pulse) * 0.3;
            ctx.beginPath();
            ctx.arc(n.x, n.y, n.r, 0, Math.PI * 2);
            ctx.fillStyle = `rgba(0,229,255,${glow})`;
            ctx.fill();
            // outer ring
            ctx.beginPath();
            ctx.arc(n.x, n.y, n.r + 3, 0, Math.PI * 2);
            ctx.strokeStyle = `rgba(0,229,255,${glow * 0.3})`;
            ctx.lineWidth = 0.5;
            ctx.stroke();
        });
        netAnimId = requestAnimationFrame(drawNet);
    }
    drawNet();
}

